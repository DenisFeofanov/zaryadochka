// GetDayWord returns the correct form of "день/дня/дней" based on count
func GetDayWord(days int) string {
	if days%100 >= 11 && days%100 <= 19 {
		return "дней"
	}

	switch days % 10 {
	case 1:
		return "день"
	case 2, 3, 4:
		return "дня"
	default:
		return "дней"
	}
}
